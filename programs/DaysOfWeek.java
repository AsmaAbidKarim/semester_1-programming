public class DaysOfWeek
{
	public static void main(String[] args)
	{
		int n=Integer.parseInt(args[0]);
		if(n==1) System.out.print("Monday");
		if(n==2) System.out.print("Tuesday");
		if(n==3) System.out.print("Wednesday");
		if(n==4) System.out.print("Thrusday");
		if(n==5) System.out.print("Friday");
		if(n==6) System.out.print("Saturday");
		if(n==7) System.out.print("Sunday");
		
			
		
	
	}
}